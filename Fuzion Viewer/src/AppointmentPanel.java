    // Clear all filter parameters and set model back to all rows
    private void btnClearSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearSearchActionPerformed
        // Clear all filter fields
        txtLookFor.setText("");
        txtLookFor.grabFocus();
        dtcFrom.setDate(null);
        dtcTo.setDate(null);
        // Clear all parameters from list
        int filtersQty = controller.clearSearchParamAppointments();
        btnClearSearch.setText("Clear (" + filtersQty + ")");
        controller.filterTable(tblDataTable, "", 0, null, null, 0);
        setTotalTable();
    }//GEN-LAST:event_btnClearSearchActionPerformed

    // Filter the table with the parameters selected on the filter fields
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        int filtersQty;
        filtersQty = controller.filterTable(tblDataTable, txtLookFor.getText(), cboInField.getSelectedIndex(), dtcFrom.getDate(), dtcTo.getDate(), cboDateFields.getSelectedIndex());
        btnClearSearch.setText("Clear (" + filtersQty + ")");
        txtLookFor.grabFocus();
        txtLookFor.selectAll();
        setTotalTable();
    }//GEN-LAST:event_btnSearchActionPerformed

