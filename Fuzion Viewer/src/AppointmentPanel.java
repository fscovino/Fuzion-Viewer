    // Export Selected data to CSV file
    private void btnExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportActionPerformed
        int success = controller.exportDataToCsv(tblDataTable, "Appointments");
        if (success == 1){
            JOptionPane.showMessageDialog(null, "Data Exported Successfully", "Data Exported", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Data Could not be Exported", "Data Exported", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnExportActionPerformed

    // Get table data and populate the table
    private void setDataTable(){
        tblDataTable.setModel(controller.getAppointmentModel());
        // Set initial column width
        for (int i = 0; i < tblDataTable.getColumnCount(); i++){
            tblDataTable.getColumnModel().getColumn(i).setPreferredWidth(125);
        }
        // Set the format for columns to display date and price properly
        for (int i = 0; i < tblDataTable.getColumnCount(); i++){
            if ((i != 2) && (i != 14) && (i != 15)) {
                tblDataTable.getColumnModel().getColumn(i).setCellRenderer(cellRender);
            }
        }
    }
    
// Generate totals for selected rows everytime a row is selected
    private void setTotalTable(){
        TableModel model = controller.getAppointmentTotalsModel(tblDataTable);
        tblTotals.setModel(model);
        
        // Set the format for columns to display date and price properly
        for (int i = 0; i < tblTotals.getColumnCount(); i++){
            tblTotals.getColumnModel().getColumn(i).setCellRenderer(cellRender);
        }
    }

